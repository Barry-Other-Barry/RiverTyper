static void drawChar(SDL_Renderer*r,int x,int y,char c){
    int idx=(c<32||c>126)?0:(c-32);
    for(int row=0;row<8;row++){
        unsigned char bits=font8x8[idx][row];
        for(int col=0;col<8;col++){
            if((bits>>(7-col))&1)SDL_RenderDrawPoint(r,x+col,y+row);
        }
    }
}
static void drawString(SDL_Renderer*r,int x,int y,const char*s){
    int dx=x;
    while(*s){
        if(*s=='\n'){y+=8;dx=x;s++;}
        else{drawChar(r,dx,y,*s);dx+=8;s++;}
    }
}
int main(int argc,char*argv[]){
    if(SDL_Init(SDL_INIT_VIDEO)!=0)return 1;
    SDL_Window*win=SDL_CreateWindow("Key Press Monitor",SDL_WINDOWPOS_CENTERED,SDL_WINDOWPOS_CENTERED,800,600,0);
    if(!win)return 1;
    SDL_Renderer*ren=SDL_CreateRenderer(win,"",SDL_RENDERER_ACCELERATED);
    if(!ren)return 1;
    bool running=true;
    bool keys[SDL_NUM_SCANCODES];
    memset(keys,false,sizeof(keys));
    while(running){
        SDL_Event e;
        while(SDL_PollEvent(&e)){
            if(e.type==SDL_EVENT_QUIT)running=false;
            if(e.type==SDL_EVENT_KEY_DOWN)keys[e.key.keysym.scancode]=true;
            if(e.type==SDL_EVENT_KEY_UP)keys[e.key.keysym.scancode]=false;
        }
        SDL_SetRenderDrawColor(ren,0,0,0,255);
        SDL_RenderClear(ren);
        char pressed[1024];
        int idx=0;
        for(int i=0;i<SDL_NUM_SCANCODES;i++){
            if(keys[i]){
                const char*name=SDL_GetKeyName(SDL_GetKeyFromScancode(i));
                for(int j=0;name[j]&&idx<1023;j++){
                    pressed[idx++]=name[j];
                }
                if(idx<1023)pressed[idx++]=' ';
            }
        }
        pressed[idx]='\0';
        SDL_SetRenderDrawColor(ren,255,255,255,255);
        drawString(ren,10,10,pressed);
        SDL_RenderPresent(ren);
    }
    SDL_DestroyRenderer(ren);
    SDL_DestroyWindow(win);
    SDL_Quit();
    return 0;
}
